package shutdown

/* ========================================================================
 * Shutdown Priority - 关停钩子优先级
 * ========================================================================
 * 职责: 定义关停钩子的优先级常量
 * 说明: 优先级数值越小，越先执行；同优先级的钩子并行执行
 * ======================================================================== */

const (
	// PriorityFirst 最高优先级 (0)
	// 用途: 停止接收新请求
	// 示例: HTTP/GRPC 服务器停止监听
	PriorityFirst = 0

	// PriorityHigh 高优先级 (100)
	// 用途: 完成进行中的请求
	// 示例: 等待活跃请求完成、关闭连接
	PriorityHigh = 100

	// PriorityNormal 普通优先级 (500)
	// 用途: 默认优先级
	// 示例: 一般资源清理
	PriorityNormal = 500

	// PriorityLow 低优先级 (800)
	// 用途: 刷新缓存、持久化数据
	// 示例: 刷新 Redis 缓存、写入日志缓冲
	PriorityLow = 800

	// PriorityLast 最低优先级 (999)
	// 用途: 关闭底层连接
	// 示例: 数据库连接、消息队列连接
	PriorityLast = 999
)
